-- SỰ KHÁC BIỆT GIỮA CHAR VÀ VARCHAR 
	-- Char là kiểu dữ liệu chuỗi có độ dài cố định. Điều này có nghĩa là tất cả các giá trị được lưu trữ trong 
	-- cột char sẽ có cùng độ dài. Độ dài của cột char được xác định bởi giá trị n trong cú pháp CHAR(n).

	-- Varchar là kiểu dữ liệu chuỗi có độ dài biên đổi. Điều này có nghĩa là các giá trị được lưu trữ trong cột
	-- varchar có thể có độ dài khác nhau. Độ dài tối đa của cột varchar được xác định bởi giá trị n trong cú pháp VARCHAR(n).



